function makeInt(n){
  return parseInt(n);
}

function preserveDecimal(n){
  return parseFloat(n);
}
function add(a, b){
  return a + b;
}

function subtract(a, b){
  return b - a
}
function multiply(a, b){
  return a * b;
}
function divide(a, b){
  return b/a;
}
function inc(n){
  return n++
}
function dec(n){
  return n--
}